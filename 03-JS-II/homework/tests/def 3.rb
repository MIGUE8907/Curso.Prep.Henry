Operadores Logicos 

Y (and) Este operador nos permite evaluar: Si las dos condiciones son true devolvera true, si alguna de las dos condiociones o 
las dos es false, devolvera false, y se escriibe &&.

O (OR) Este operador, nos permite evaluar dos expresiones asi: si las dos son true, o alguna de las dos es true, devolvera true,
devolver true, si las dos son false, devolverda false y se escribe con dos barras verticales || 

NO (NOT) Este operador devolvera la expreion contraria, como por ejemplo si el codigo da true se convertira en false, 
y si es false se convrtira en true. se escribe !== teniendo en cuenta que el signo de expresion (!) siempre se encuentra a la 
izquierda del ==.

